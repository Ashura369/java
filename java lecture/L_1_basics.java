public class L_1_basics{
    public static void main(String[] args){
        System.out.print("hello world 1 "); // you can use '\n' to put new line, but you have to use it like "text \n" ("hello world 1 \n")
        System.out.println("hello world 2"); // you can use 'print'/'println'
        System.out.print("hello world 1 "); // you can use '\n' to put new line
        // java is case sensitive, keep that in mind. Be mindful of the words you put in java

        /* 
         * 'print' -- prints everything 
         * 'println' -- prints everything in a new line
         * 
         * hence here outputs will be printed as 
         * "hello world 1 hello world 2
            hello world 1 "
         */
    }
}